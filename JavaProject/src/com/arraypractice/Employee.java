package com.arraypractice;

public class Employee {
	int id, salary;
	String name;
	Date d;

	public void setId(int id) {
		this.id = id;

	}

	public int getId() {
		return id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setSalary(int salary) {
		this.salary = salary;
	}

	public int getSalary() {
		return salary;
	}

	public void setD(Date d) {
		this.d=d;
	}

	public Date getD() {
		return d;
	}
}
