package com.basicprogram;
public class ArithmeticOperation {

	public static void main(String[] args) {
		int a=5,b=10;
		int sum=a+b;
		System.out.println("sum:"+sum);
		int sub=a-b;
		System.out.println("sub:"+sub);
		int mul=a*b;
		System.out.println("mul:"+mul);
		int div=b/a;
		System.out.println("div:"+div);
	}

}
