package com.practice;
//Encapsulation
public class Bag {
private String books,notebook;
private int noofpen,noofpencils;
	public void setBooks(String books)
	{
		this.books=books;
	}
	public String getBooks()
	{
		return books;
	}
	public void setNotebooks(String notebook)
	{
		this.notebook=notebook;
	}
	public String getNotebook()
	{
		return notebook;
	}
	public void setNoofpen(int noofpen)
	{
		this.noofpen=noofpen;
	}
	public int getNoofpen()
	{
		return noofpen;
	}
	public void setNoofpencils(int noofpencils)
	{
		this.noofpencils=noofpencils;
	}
	public int getNoofpencils()
	{
		return noofpencils;
	}
}
